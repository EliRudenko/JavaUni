# Приклад оголошення функції без параметрів і без повернення значення.
# Ця версія нижче буде перевизначена, що демонструє відсутність
# класичного перевантаження функцій у Python.
def print_hello() -> None :
    print("Hello")  # Вивід рядка у стандартний потік.


# Приклад оголошення функції з параметром.
# У Python немає перевантаження за сигнатурою: останнє визначення
# з таким же ім'ям "перекриває" попередні.
def print_hello(x:str) -> None :
    print("Hi", x)  # Вивід рядка та аргументу.


# Функція з параметром за замовчуванням.
# Саме ця версія залишиться активною після всіх трьох визначень.
def print_hello(x:str='') -> None :
    print("Hola", x)  # Використання значення за замовчуванням, якщо аргумент не передано.


# Функція з іменованими параметрами.
def greet(phrase:str='Hello', who:str='All') -> None :
    print(phrase, who)  # Демонстрація іменованих та позиційних аргументів.

# Глобальна змінна модуля (область видимості: модуль).
x = 10

# Читання глобальної змінної без ключового слова global.
def get_x() -> int :
    return x  # Повертаємо значення глобальної змінної.


# Спроба змінити глобальну змінну без global створить локальну змінну x.
def set_x(new_x:int) -> None :
    x = new_x  # Це ЛОКАЛЬНА змінна, глобальна x не змінюється.
    print ("X set to", x)  # Виводимо локальне значення.


# Правильне оновлення глобальної змінної через ключове слово global.
def set_global_x(new_x:int) -> None :
    global x  # Вказуємо, що працюємо з глобальною x.
    x = new_x  # Змінюємо глобальну змінну.


# Рекурсивна функція показує повторення (цикли) через виклик самої себе.
